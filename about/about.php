<h2>For Prospective Students (Prefrosh)</h2>
<h3>
    Ultimate can be one of the best experiences you'll have at Brown, and we are always excited
    to welcome new students to the program.<br> Interested in playing ultimate at Brown? Let us
    know when you'll be on campus, and we'll arrange for you to meet some of the team and come
    to one of our practices. We're also happy to answer any of your questions over email, whether
    about ultimate or about life at Brown in general. You can email either one of the captains
    from the contact information above.<br>

    Brownian Motion has all types on its team. Short guys to taller ones. Former soccer players to
    basketball players to track athletes. Guys who never played ultimate before college to guys who
    played in a summer league or played at their high school. Come in the fall in shape and show
    dedication and we'll take care of the rest. Brown has three different ultimate teams with
    different levels of commitment: the A team (Brownian Motion), B team (P Funk), and C team
    (Mothership). Each year we have an instructional, relaxed fall league for all new and returning
    members of the program, before cuts for the teams are made. It gives everyone a chance to show
    their stuff and pick up some new skills. Anyone can come to fall league, so look for
    announcements about playing ultimate in early September.<br>
    
    Each year at ADOCH, there is a special late-night ultimate frisbee event, and we would love
    to see you there! Current team members put on the event, so come out to play with us and ask
    us anything. Also at ADOCH, during the Extracurricular Fair, there is a table for ultimate
    frisbee, and this is another great way to put your name on a mailing list and hear more
    about playing ultimate at Brown.
</h3>
